<h1><strong>Route</strong></h1>
<p>The "P to P Triathlon" will be be almost a Half Ironman, consisting of: a run of roughly 16 miles,
    a challenging swim of 3 miles and a scenic cycling course of 25 miles.<br/> The race will begin near San Quentin,
    run to Fisherman's wharf, swim to Alcatraz island and back and then bike around San Francisco bay, 
    crossing the John T. Knox freeway and ending where the race began. </p>
